// Юнит-тесты чистой стрик-логики (без IO, без БД). Покрывают требуемые кейсы:
// пустой список, повтор сегодня, сегодня-пусто-вчера-было (стрик жив), разрыв.
import { describe, it, expect } from "vitest";
import { computeStreak } from "./streak";

describe("computeStreak", () => {
  it("пустой список → 0", () => {
    expect(computeStreak([], "2026-07-06")).toBe(0);
  });

  it("повтор сегодня → цепочка считает сегодняшний день", () => {
    expect(computeStreak(["2026-07-06"], "2026-07-06")).toBe(1);
  });

  it("сегодня без повтора, вчера было → стрик жив (считаем от вчера)", () => {
    expect(computeStreak(["2026-07-05"], "2026-07-06")).toBe(1);
  });

  it("цепочка подряд, заканчивая сегодня → её длина", () => {
    expect(computeStreak(["2026-07-04", "2026-07-05", "2026-07-06"], "2026-07-06")).toBe(3);
  });

  it("цепочка подряд до вчера (сегодня пусто) → жива и полна", () => {
    expect(computeStreak(["2026-07-03", "2026-07-04", "2026-07-05"], "2026-07-06")).toBe(3);
  });

  it("разрыв перед сегодняшним днём останавливает счёт", () => {
    expect(computeStreak(["2026-07-01", "2026-07-02", "2026-07-06"], "2026-07-06")).toBe(1);
  });

  it("ни сегодня, ни вчера → 0 (стрик разорван)", () => {
    expect(computeStreak(["2026-07-01", "2026-07-02", "2026-07-03"], "2026-07-06")).toBe(0);
  });

  it("дубли одного дня не раздувают стрик", () => {
    expect(computeStreak(["2026-07-06", "2026-07-06", "2026-07-05"], "2026-07-06")).toBe(2);
  });

  it("порядок дат в списке не важен", () => {
    expect(computeStreak(["2026-07-05", "2026-07-06", "2026-07-04"], "2026-07-06")).toBe(3);
  });

  it("граница месяца (UTC) считается корректно", () => {
    expect(computeStreak(["2026-06-30", "2026-07-01"], "2026-07-01")).toBe(2);
  });
});
