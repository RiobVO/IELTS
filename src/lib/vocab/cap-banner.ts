/**
 * Баннер дневного лимита новых карт (чистая логика — cap-banner.test.ts).
 * Показываем ТОЛЬКО когда кап реально мешает: остаток новых = 0 И впереди в очереди
 * ещё стоят новые (не начатые) карты, которые сервер отобьёт. Повторы уже начатых
 * карт капом не ограничены — баннер поверх них читался как противоречие («лимит
 * исчерпан, но всё работает») и вылезал даже при перезаходе в чисто-повторную
 * сессию. null = безлимитный тир, баннер не показывается никогда.
 */
export function shouldShowCapBanner(
  newRemainingToday: number | null,
  upcoming: ReadonlyArray<{ isNew: boolean }>,
): boolean {
  return newRemainingToday === 0 && upcoming.some((c) => c.isNew);
}
